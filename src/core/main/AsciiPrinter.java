    public void print(String letters, int template)
    {
        Boolean defaultTemplate = template == 1;
        ArrayList<String> templateUsed = defaultTemplate ? smallTemplate : bigTemplate;
        
        letters = letters.toUpperCase();
        
        switch (template) 
        {
            default:
            case 1:
                this.width = 4;
                this.height = 5;
                break;
            case 2:
                this.width = 20;
                this.height = 11;
                break;
        }
        
        ArrayList<Integer> indexes = new ArrayList<Integer>();
        for(char c : letters.toCharArray()) {
            if(c < 'A' || c > 'Z') indexes.add(26);
            else {
                for(char letter : alphabet.toCharArray()) {
                    if(c == letter) {
                        indexes.add(alphabet.indexOf(letter));
                    }
                }
            }
        }
        
        for(int j = 0; j < height; j++) {
            for(int k = 0; k < indexes.size(); k++) {
                System.out.print(templateUsed.get(j).substring(indexes.get(k)*width, indexes.get(k)*width + width));
            }
            System.out.println();
        }
    }
